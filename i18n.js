// Apply the chosen language to the current lesson page.
// Reads the lesson ID from <meta name="lesson-id"> to set the page title.
// ─────────────────────────────────────────────────────────────────────────────
function setLanguage(lang) {
  const dict = lessonTranslations[lang];
  if (!dict) return;

  // Replace plain text (no inner HTML tags)
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.textContent = dict[key];
    }
  });

  // Replace HTML content (element contains <strong>, <code>, <br>, etc.)
  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update the browser tab title using the lesson-id meta tag
  const lessonMeta = document.querySelector('meta[name="lesson-id"]');
  if (lessonMeta) {
    const titleKey = lessonMeta.content + '.page.title';
    if (dict[titleKey]) {
      document.title = dict[titleKey];
    }
  }

  // Update <html lang=""> for screen readers and SEO
  document.documentElement.lang = lang;

  // Highlight the active button in the switcher (if one exists on the page)
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.classList.toggle('active', btn.textContent.toLowerCase() === lang);
  });

  // Persist the choice so the next lesson page also uses it
  localStorage.setItem('language', lang);
}

// ─────────────────────────────────────────────────────────────────────────────
// On page load: restore the previously saved language, or default to Russian.
// ─────────────────────────────────────────────────────────────────────────────
(function init() {
  const savedLanguage = localStorage.getItem('language') || 'ru';
  setLanguage(savedLanguage);
})();
